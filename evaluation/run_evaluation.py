def load_golden_dataset(path: str) -> list:
    """
    Loads human-authored golden dataset.

    Why human-authored and not LLM-generated?
    If we used the LLM to generate ground truth answers,
    we would evaluate the LLM against itself — measuring
    self-consistency, not accuracy. Ground truth must be
    written by a human reading the actual document.

    This is what the Aziro interviewer asked about —
    the process of creating the evaluation dataset.

    Interview answer:
    "We created 20 Q&A pairs manually across 8 categories.
    Ground truth was written by a human reading the contract
    directly — not generated by any LLM. We included edge
    cases where the answer is genuinely not in the document
    to test honest refusal behaviour. Using LLM-generated
    ground truth creates circular evaluation that measures
    self-consistency rather than actual accuracy."
    """
    with open(path, "r") as f:
        return json.load(f)


def setup_ragas(llm_client):
    """
    Sets up RAGAS 0.4.3 collections metrics.

    Faithfulness uses LLM-as-judge:
    - Decomposes answer into atomic statements
    - Checks each statement against retrieved context
    - Returns proportion of supported statements

    AnswerRelevancy uses embeddings:
    - Generates synthetic questions from the answer
    - Measures semantic similarity to original question
    - Returns relevancy score 0 to 1
    """

    llm = llm_factory(
        "llama-3.1-8b-instant",
        provider="openai",
        client=llm_client
    )

    # RAGAS native HuggingFace embeddings
    ragas_embeddings = RagasHFEmbeddings(
        model="sentence-transformers/all-MiniLM-L6-v2"
    )

    faithfulness_metric = Faithfulness(llm=llm)

    relevancy_metric = AnswerRelevancy(
        llm=llm,
        embeddings=ragas_embeddings
    )

    return faithfulness_metric, relevancy_metric


async def score_faithfulness(
    metric,
    question: str,
    answer: str,
    contexts: list
) -> float:
    """
    Scores faithfulness using RAGAS 0.4.3 collections API.
    Collections API: await metric.ascore(user_input, response, retrieved_contexts)
    Returns MetricResult with .value property.
    """
    try:
        result = await metric.ascore(
            user_input=question,
            response=answer,
            retrieved_contexts=contexts
        )
        if hasattr(result, 'value'):
            return round(float(result.value), 3)
        return round(float(result), 3)
    except Exception as e:
        print(f"    [Warning] Faithfulness error: {e}")
        return 0.0


async def score_relevancy(
    metric,
    question: str,
    answer: str
) -> float:
    """
    Scores answer relevancy using RAGAS 0.4.3 collections API.
    AnswerRelevancy only needs user_input and response.
    No retrieved_contexts needed for this metric.
    """
    try:
        result = await metric.ascore(
            user_input=question,
            response=answer
        )
        if hasattr(result, 'value'):
            return round(float(result.value), 3)
        return round(float(result), 3)
    except Exception as e:
        print(f"    [Warning] Relevancy error: {e}")
        return 0.0


async def run_evaluation(
    contract_path: str,
    golden_dataset_path: str,
    output_path: str
):
    """
    Complete end-to-end evaluation pipeline.

    Why evaluate full pipeline not just the LLM?
    We measure end-to-end system quality including retrieval.
    If retrieval fails, even a perfect LLM gives wrong answers.

    RAGAS metrics:
    Faithfulness     = is answer grounded in retrieved context?
                       Measures hallucination risk.
                       Target: above 0.80

    AnswerRelevancy  = does answer address the question?
                       Measures usefulness to the user.
                       Target: above 0.75
    """

    print("=" * 55)
    print("RAGAS EVALUATION PIPELINE")
    print(f"Contract: {contract_path}")
    print(f"Dataset: {golden_dataset_path}")
    print("=" * 55)

    # Pre-load embedder singleton
    print("\nPre-loading embedder...")
    get_embedder()
    print("Embedder ready.")

    # Ingest contract
    print("\nIngesting contract...")
    ingest_result = ingest_contract(contract_path)
    session_id = ingest_result["session_id"]
    print(f"Session ID: {session_id}")

    # Setup RAGAS
    print("\nSetting up RAGAS judge LLM and embeddings...")
    groq_api_key = os.getenv("GROQ_API_KEY")
    llm_client = AsyncOpenAI(
        api_key=groq_api_key,
        base_url="https://api.groq.com/openai/v1"
    )
    faithfulness_metric, relevancy_metric = setup_ragas(llm_client)
    print("RAGAS ready.")

    # Load golden dataset
    print("\nLoading golden dataset...")
    golden_data = load_golden_dataset(golden_dataset_path)
    print(f"Loaded {len(golden_data)} test cases")

    # Run evaluation
    print("\nRunning evaluation...")
    print("-" * 55)

    results = []

    for i, item in enumerate(golden_data, 1):
        question = item["question"]
        ground_truth = item["ground_truth"]
        category = item["category"]

        print(f"\n[{i}/{len(golden_data)}] {category.upper()}")
        print(f"  Q: {question[:70]}...")

        # Get answer from our RAG system
        response = get_answer(question, session_id)
        answer = response["answer"]
        status = response["status"]

        print(f"  Status: {status}")
        print(f"  A: {answer[:100]}...")

        # Get retrieved contexts for faithfulness scoring
        try:
            retriever = get_retriever(session_id)
            docs = retriever.invoke(question)
            contexts = [doc.page_content for doc in docs]
        except Exception:
            contexts = [answer]

        # Score with RAGAS
        if status == "success" and len(answer) > 20:
            print("  Scoring with RAGAS...")

            faithfulness_score = await score_faithfulness(
                faithfulness_metric,
                question,
                answer,
                contexts
            )

            relevancy_score = await score_relevancy(
                relevancy_metric,
                question,
                answer
            )

        else:
            # Correct refusal on edge cases
            faithfulness_score = 1.0 if category == "not_in_document" else 0.0
            relevancy_score = 0.5 if category == "not_in_document" else 0.0
            print("  Skipping RAGAS scoring (refusal or blocked)")

        result = {
            "id": item["id"],
            "category": category,
            "difficulty": item["difficulty"],
            "question": question,
            "ground_truth": ground_truth,
            "system_answer": answer,
            "status": status,
            "faithfulness": faithfulness_score,
            "answer_relevancy": relevancy_score,
            "confidence": response.get("confidence", 0.0),
            "latency_seconds": response.get("latency_seconds", 0.0)
        }

        results.append(result)

        print(f"  Faithfulness    : {faithfulness_score:.3f}")
        print(f"  Answer Relevancy: {relevancy_score:.3f}")

    # Calculate summary
    print("\n" + "=" * 55)
    print("EVALUATION SUMMARY")
    print("=" * 55)

    total = len(results)
    successful = [r for r in results if r["status"] == "success"]

    avg_faithfulness = round(
        sum(r["faithfulness"] for r in results) / total, 3
    )
    avg_relevancy = round(
        sum(r["answer_relevancy"] for r in results) / total, 3
    )
    avg_latency = round(
        sum(r["latency_seconds"] for r in results) / total, 2
    )
    success_rate = round(len(successful) / total, 3)

    # By category
    categories = set(r["category"] for r in results)
    category_scores = {}
    for cat in categories:
        cat_results = [r for r in results if r["category"] == cat]
        category_scores[cat] = {
            "faithfulness": round(
                sum(r["faithfulness"] for r in cat_results) / len(cat_results), 3
            ),
            "answer_relevancy": round(
                sum(r["answer_relevancy"] for r in cat_results) / len(cat_results), 3
            ),
            "count": len(cat_results)
        }

    summary = {
        "evaluation_date": datetime.now().isoformat(),
        "contract": contract_path,
        "model_used": "llama-3.1-8b-instant",
        "total_questions": total,
        "successful_answers": len(successful),
        "success_rate": success_rate,
        "avg_faithfulness": avg_faithfulness,
        "avg_answer_relevancy": avg_relevancy,
        "avg_latency_seconds": avg_latency,
        "by_category": category_scores,
        "results": results
    }

    # Save results
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(summary, f, indent=2)

    # Print final report
    print(f"\nTotal questions     : {total}")
    print(f"Successful answers  : {len(successful)}")
    print(f"Success rate        : {success_rate:.1%}")
    print(f"\nAvg Faithfulness    : {avg_faithfulness:.3f}")
    print(f"Avg Answer Relevancy: {avg_relevancy:.3f}")
    print(f"Avg Latency         : {avg_latency:.2f}s")

    print(f"\nBy category:")
    for cat, scores in sorted(category_scores.items()):
        print(
            f"  {cat:<25} "
            f"faithfulness: {scores['faithfulness']:.3f} | "
            f"relevancy: {scores['answer_relevancy']:.3f} | "
            f"({scores['count']} Qs)"
        )

    print(f"\nResults saved to: {output_path}")
    print("=" * 55)

    return summary


if __name__ == "__main__":
    asyncio.run(run_evaluation(
        contract_path="data/contracts/vendor.pdf",
        golden_dataset_path="evaluation/golden_dataset.json",
        output_path="evaluation/results.json"
    ))